public class Pattern_1 {
	
	public static void main(java.lang.String []arg) {
		
		for( int i =10 ; i >= 0; i-- ) {
			for(int j=i; j<10; j++) {
				System.out.print("*");
			}
			System.out.println();
				
			
		}
	}
}
